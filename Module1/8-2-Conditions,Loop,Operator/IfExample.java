package com.a82;

public class IfExample
{
	
	static int marks = 65;
	
	public static void main(String[] args)
	{
	
			
			
			if(marks>=70)
			{
				System.out.println("A Grade");
			}
			else if(marks>=60)
			{
				System.out.println("B Grade");
			}
			else if(marks>=50)
			{
				System.out.println("C Grade");
			}
			else if(marks>=40)
			{
				System.out.println("D Grade");
			}
			else
			{
				System.out.println("Fail");
			}
		
		
	}
	
}
